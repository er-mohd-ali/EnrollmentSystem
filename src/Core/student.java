public class student {
    
private String firstname,lastname,primarycon,secondarycon,email,address,username,password;  

    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }

    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }

    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    /**
     * @return the primarycon
     */
    public String getPrimarycon() {
        return primarycon;
    }

    /**
     * @param primarycon the primarycon to set
     */
    public void setPrimarycon(String primarycon) {
        this.primarycon = primarycon;
    }

    /**
     * @return the secondarycon
     */
    public String getSecondarycon() {
        return secondarycon;
    }

    /**
     * @param secondarycon the secondarycon to set
     */
    public void setSecondarycon(String secondarycon) {
        this.secondarycon = secondarycon;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }
}
